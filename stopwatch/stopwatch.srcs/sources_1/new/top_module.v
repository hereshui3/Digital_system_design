`timescale 1ns / 1ps  // 时间单位1纳秒，时间精度1皮秒

// 顶层模块：秒表
module mb(
    input clk,        // 系统时钟输入
    input key1,       // 计时控制按键(1=计时，0=暂停)
    input rst_n,      // 复位信号(低电平有效)
    output [3:0] sm_wei,  // 数码管位选信号(控制4位数码管中的哪一位亮)
    output [7:0] sm_duan  // 数码管段选信号(控制显示的数字或字符)
);

//----------------------------------------------------------
// 内部信号定义
wire [15:0] data;     // 16位计时数据总线(4位BCD码×4)
wire [3:0] sm_wei;    // 数码管位选信号线
wire [7:0] sm_duan;   // 数码管段选信号线
//----------------------------------------------------------

// 实例化计时器模块
// 端口映射：
// clk    - 系统时钟
// rst_n  - 复位信号
// key1   - 计时控制
// data   - 输出的计时数据(16位)
time_counter U0(
    .clk(clk),      // 连接系统时钟
    .rst_n(rst_n),  // 连接复位信号
    .key1(key1),    // 连接计时控制按键
    .data(data)     // 输出计时数据
);

// 实例化数码管显示模块
// 端口映射：
// clk     - 系统时钟
// data    - 输入的计时数据
// sm_wei  - 位选控制信号
// sm_duan - 段选控制信号
smg_ip U1(
    .clk(clk),       // 连接系统时钟
    .data(data),     // 连接计时数据
    .sm_wei(sm_wei), // 输出位选信号
    .sm_duan(sm_duan) // 输出段选信号
);

endmodule